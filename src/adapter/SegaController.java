package adapter;

public class DualSense {

    public void joyStickUp(){
        System.out.println("↑");
    }

    public void joyStickDown(){
        System.out.println("↓");
    }

    public void joyStickLeft(){
        System.out.println("←");
    }

    public void joyStickRight(){
        System.out.println("→");
    }

    public void pressX(){
        System.out.println("Action");
    }

    public void pressO(){
        System.out.println("Jump");
    }

}
